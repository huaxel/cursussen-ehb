let punt = 19;

if(punt >= 18){
    console.log('Geslaagd met felicitaties van de jury');
}else if(punt >= 16){
    console.log('Geslaagd met grote onderscheiding');
}else if(punt >= 14){
    console.log('Geslaagd met onderscheiding');
}else if(punt >= 10){
    console.log('Geslaagd');
}else if(punt >= 8){
    console.log('Getolereerd');
}else{
    console.log('Niet geslaagd');
}